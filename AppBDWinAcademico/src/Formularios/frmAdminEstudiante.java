    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
          Estudiante obj = null;
        EstudianteDAO Oest = new EstudianteDAO();
        if (jTable1.getSelectedRow() >= 0) {
            try {
                int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                obj = Oest.SearchEstudiante(id);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }

            frmInsertarEstudiante me = new frmInsertarEstudiante("Modificar");
            me.setDatos(obj);
            me.show();
        } else {
            JOptionPane.showMessageDialog(null, "No se ha seleccionado nada");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Estudiante oe=null;  
        EstudianteDAO Oest = new EstudianteDAO();
        if(jTable1.getSelectedRow()>=0){
            if(JOptionPane.showConfirmDialog(null, "Â¿Desea eliminar el Estudiante de forma permanente?")==JOptionPane.YES_OPTION)
            {         
                try{
                    int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                    oe = Oest.SearchEstudiante(id);
                    Oest.Delete(oe);
               }catch(Exception ex)
               {
                   JOptionPane.showMessageDialog(null, ex.getMessage());
               }
                MostrarDatos("");
            }
        }else{
            JOptionPane.showMessageDialog(null, "No se ha seleccionado nada");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

