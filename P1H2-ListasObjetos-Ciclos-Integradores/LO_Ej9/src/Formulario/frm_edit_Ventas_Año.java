    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jComboBox1.getSelectedIndex() >= 0 && jComboBox2.getSelectedIndex() >= 0 && !jTextField1.getText().equals("") && !jTextField2.getText().equals("") && !jTextField3.getText().equals("")) {

            int Id=Integer.parseInt(jTextField1.getText());
            int dia=Integer.parseInt(jComboBox1.getSelectedItem().toString());
            int mes=Integer.parseInt(jComboBox2.getSelectedItem().toString());;
            int año=Integer.parseInt(jComboBox3.getSelectedItem().toString());;
            int cantFact=Integer.parseInt(jTextField2.getText());
            double importe=Double.parseDouble(jTextField3.getText());;
            
            LO_Ventas_Año oc = new LO_Ventas_Año(Id, dia, mes, año, cantFact, importe);
            TL_Ventas_Año.Insert(oc);
            frm_Admin_LO_Ventas_Año.VisualizarLista();
            this.dispose();
            
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

