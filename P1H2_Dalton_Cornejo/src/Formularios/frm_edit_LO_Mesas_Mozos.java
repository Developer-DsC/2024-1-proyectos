    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String nombre = jComboBox2.getSelectedItem().toString();
        int mesa = jComboBox1.getSelectedIndex();
        double importe = Double.parseDouble(jTextField3.getText());
        int cantidad = Integer.parseInt(jTextField4.getText());
        int Idvent = Integer.parseInt(jTextField1.getText());
        LO_Mesas_Mozos oe = new LO_Mesas_Mozos(Idvent, nombre, mesa, importe, cantidad);

        if (Funcion.equals("Insertar")) {
            TL_Mesas_Mozos.Insertar(oe);
        }

        if (Funcion.equals("Modificar")) {

            mesa = jComboBox1.getSelectedIndex();
            importe = Double.parseDouble(jTextField3.getText());
            cantidad = Integer.parseInt(jTextField4.getText());
            Idvent = Integer.parseInt(jTextField1.getText());

            jTextField1.setEnabled(false);
            TL_Mesas_Mozos.Modificar(TL_Mesas_Mozos.Buscar(Idvent), oe);
        }
        
        frm_Admin_LO_Mesas_Mozos.Listar();
        this.dispose();

    }//GEN-LAST:event_jButton2ActionPerformed

