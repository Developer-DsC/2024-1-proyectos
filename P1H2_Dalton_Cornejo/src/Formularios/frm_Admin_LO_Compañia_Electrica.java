    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTable1.getSelectedRow() >= 0) {
            String ced = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
            int pos = TL_Compañia_Electrica.Search(ced)+1;
            LO_Compañia_Electrica obj = TL_Compañia_Electrica.getFactura(pos);

            frm_edit_LO_Compañia_Electrica frm = new frm_edit_LO_Compañia_Electrica("Modificar");
            frm.setDatos(obj);
            frm.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una Fila");
        }
        MostrarDatos();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTable1.getSelectedRow() >= 0) {

            int ip = JOptionPane.showConfirmDialog(null, "Desea eliminar registro", "Eliminar Factura", JOptionPane.YES_NO_OPTION);
            if (ip == JOptionPane.YES_OPTION) {
                String ced = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
                int pos = TL_Compañia_Electrica.Search(ced)+1;
                TL_Compañia_Electrica.Delete(pos);
                MostrarDatos();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        }
        MostrarDatos();
    }//GEN-LAST:event_jButton3ActionPerformed

