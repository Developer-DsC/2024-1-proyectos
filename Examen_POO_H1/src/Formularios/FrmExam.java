    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if (jComboBox1.getSelectedItem().toString().equals("Nombramiento")) {
            jLabel9.setText("Salario Mensual: ");
            jLabel10.setText("Anticipo: ");
            jTextField7.setEnabled(true);
            jLabel10.setEnabled(true);
        }
        if (jComboBox1.getSelectedItem().toString().equals("Comisionado")) {
            jTextField7.setText("0");
            jLabel9.setText("Salario base: ");
            jLabel10.setEnabled(false);
            jTextField7.setEnabled(false);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String idCod = jTextField1.getText();
        String Nombre = jTextField2.getText();
        String Apelli = jTextField3.getText();
        String Sexo = jTextField4.getText();
        String fechanac = jTextField5.getText();
        String estado = jComboBox2.getSelectedItem().toString();
        String tipo = jComboBox1.getSelectedItem().toString();
        
        
        
        if (tipo.equals("Nombramiento")) {
            double sueldo = Double.parseDouble(jTextField6.getText());
            double anticipo = Double.parseDouble(jTextField7.getText());
            if(anticipo>105)
        {
            JOptionPane.showMessageDialog(null, "Error el anticipo no puede ser mayor al sueldo");
        }
            op2 = new Nombramiento(sueldo, anticipo, tipo, idCod, Nombre, Apelli, Sexo, fechanac, estado);
            
            
            op2.Imprimir();
            
        }
       
        
        
        

        if (tipo.equals("Comisionado")) {
            double sueldo = Double.parseDouble(jTextField6.getText());
            op1 = new Comisionados(sueldo, tipo, idCod, Nombre, Apelli, Sexo, fechanac, estado);
           
            
            op1.Imprimir();
        }
        
        
        


    }//GEN-LAST:event_jButton1ActionPerformed

