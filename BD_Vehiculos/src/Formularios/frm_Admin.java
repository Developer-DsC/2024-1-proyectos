    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Vehiculo ovh = null;
        VehiculoDAO ovdao = new VehiculoDAO();

        if (jTable1.getSelectedRow() >= 0) {
            if (JOptionPane.showConfirmDialog(null, "¿Desea eliminar los datos del vehículo de forma permanente?") == JOptionPane.YES_OPTION) {

                int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                try {
                    ovh = ovdao.BuscarVehiculo(id);
                } catch (SQLException ex) {
                    Logger.getLogger(frm_Admin.class.getName()).log(Level.SEVERE, null, ex);
                }
                ovdao.EliminarVehiculo(ovh);

                MostrarVehiculo();
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se ha seleccionado nada");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

