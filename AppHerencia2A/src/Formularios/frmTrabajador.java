    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ced = jTextField1.getText();
        String nom = jTextField2.getText();
        String ape = jTextField3.getText();
        char sex = jComboBox1.getSelectedItem().toString().charAt(0);
        Date fn = jDateChooser1.getDate();
        String est = jComboBox2.getSelectedItem().toString();
        String tip = jComboBox3.getSelectedItem().toString();
        double v1 = Double.parseDouble(jTextField4.getText());
        double v2 = Double.parseDouble(jTextField5.getText());

        if (tip.equals("Fijo")) {
            of = new EmpleadoFijo(ced, nom, ape, sex, fn, est, v1, v2);
            Tlistafijo.ListaFijo.add(of);
        }
        if (tip.equals("Jornalero")) {
            oe = new EmpleadoJornalero(ced, nom, ape, sex, fn, est, v1, v2);
            Tlistajornalero.ListaJornalero.add(oe);
        }

        JOptionPane.showMessageDialog(null, "Ingreso exitoso!");
    }//GEN-LAST:event_jButton1ActionPerformed

