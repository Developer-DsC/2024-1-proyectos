    public static void MostrarVehiculo() {
        VehiculoDAO VEDAO = new VehiculoDAO();
        try {
            List<Vehiculo> Lista = VEDAO.ListarVehiculo();
            jTable1.setModel(VEDAO.MostrarTablaVehiculo(Lista));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

