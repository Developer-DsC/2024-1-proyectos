    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        if (jTable1.getSelectedRow() >= 0) {
            String numU = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
            int pos = TL_Empresa_Electrica.Search(numU);
            LO_Empresa_Electrica obj = TL_Empresa_Electrica.getEmpresaElectrica(pos);
            frm_edit_LO_Empresa_Electrica frm = new frm_edit_LO_Empresa_Electrica("Modificar");
            frm.jLabel7.setText("Modificar");
            frm.setDatos(obj);
            frm.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Escoja la fila a Modificar");
        }
    }//GEN-LAST:event_ModificarActionPerformed

    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        if (jTable1.getSelectedRow() >= 0) {
            int opc = JOptionPane.showConfirmDialog(null, "Desea elimar registro", "Eliminar Registro", JOptionPane.YES_NO_OPTION);
            if (opc == JOptionPane.YES_OPTION) {
                String numU = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
                int pos = TL_Empresa_Electrica.Search(numU);
                TL_Empresa_Electrica.Delete(pos);
                MostrarDatos();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Escoja la fila a Eliminar");
        }
    }//GEN-LAST:event_EliminarActionPerformed

