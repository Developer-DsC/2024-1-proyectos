    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (jComboBox3.getSelectedIndex() == 1) {
            TlistaEquipo.concentracion();
        }

        if (jComboBox3.getSelectedIndex() == 2) {
            TlistaEquipo.viaje();
        }

        if (jComboBox3.getSelectedIndex() == 3) {
            TlistaEquipo.entrenamiento();
        }

        if (jComboBox3.getSelectedIndex() == 4) {
            TlistaEquipo.partido();
        }

        if (jComboBox1.getSelectedIndex() == 1) {
            if (jComboBox3.getSelectedIndex() == 5) {
                TlistaEquipo.entrevista();
            }
        }

        if (jComboBox1.getSelectedIndex() == 2) {
            if (jComboBox3.getSelectedIndex() == 5) {
                TlistaEquipo.masaje();
            }
        }

        if (jComboBox1.getSelectedIndex() == 3) {
            if (jComboBox3.getSelectedIndex() == 5) {
                TlistaEquipo.planificarPartido();
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if (jComboBox1.getSelectedIndex() > 0) {
            jButton1.setEnabled(true);
        } else {
            jButton1.setEnabled(false);
        }

        if (jComboBox1.getSelectedIndex() == 1) {
            jComboBox3.setModel(new DefaultComboBoxModel<>(new String[]{"<Seleccione una opción> ", "Concentrarse", "Viajar", "Entrenar", "Jugar Partido", "Entrevista"}));
        }

        if (jComboBox1.getSelectedIndex() == 2) {
            jComboBox3.setModel(new DefaultComboBoxModel<>(new String[]{"<Seleccione una opción> ", "Concentrarse", "Viajar", "Entrenar", "Jugar Partido", "Dar masaje"}));

        }

        if (jComboBox1.getSelectedIndex() == 3) {
            jComboBox3.setModel(new DefaultComboBoxModel<>(new String[]{"<Seleccione una opción> ", "Concentrarse", "Viajar", "Entrenar", "Jugar Partido", "Planificar partido"}));
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

