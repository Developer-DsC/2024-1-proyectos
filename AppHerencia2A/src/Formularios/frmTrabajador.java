    private void jComboBox3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox3ActionPerformed
        // TODO add your handling code here:
        if (jComboBox3.getSelectedItem().toString().equals(" ")) {
            JOptionPane.showMessageDialog(null, "Seleccione un tipo de trabajador");
        }
        if (jComboBox3.getSelectedItem().toString().equals("Fijo")) {
            jLabel9.setText("Sueldo:");
            jLabel10.setText("Comision:");
        }
        if (jComboBox3.getSelectedItem().toString().equals("Jornalero")) {
            jLabel9.setText("Horas:");
            jLabel10.setText("Tarifa:");
        }
    }//GEN-LAST:event_jComboBox3ActionPerformed
