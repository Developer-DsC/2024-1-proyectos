    public boolean Validar() {
        if (jTextField1.getText().equals("") && jTextField2.getText().equals("") && jTextField3.getText().equals("") && jTextField4.equals("")) {
            return false;
        }
        return true;
    }

    public void Limpiar() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jSpinField1.setValue(0);
        jSpinField2.setValue(0);
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (Validar()) {
            int id = Integer.valueOf(jTextField1.getText());
            String nom = jTextField2.getText();
            String ape = jTextField3.getText();
            int edad = jSpinField1.getValue();

            SeleccionFutbol sf = null;

            if (Clase.equals("Futbolista")) {
                sf = new Futbolista(id, nom, ape, edad, jSpinField2.getValue(), jTextField4.getText());
            }
            if (Clase.equals("Entrenador")) {
                sf = new Entrenador(id, nom, ape, edad, jSpinField2.getValue());
            }
            if (Clase.equals("Masajista")) {
                sf = new Masajista(id, nom, ape, edad, jTextField4.getText(), jSpinField2.getValue());
            }
            if (Funcion.equals("Insertar")) {
                TlistaEquipo.Insert(sf);
            }
            if (Funcion.equals("Modificar")) {
                TlistaEquipo.Edit(sf, TlistaEquipo.Buscar(id));
            }

            this.hide();
            Limpiar();
            try {
                frmTabla.listarTabla(Clase);
            } catch (NullPointerException ex) {

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

