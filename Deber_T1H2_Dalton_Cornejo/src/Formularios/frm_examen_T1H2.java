    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String idcod = jTextField1.getText();
        String nom = jTextField2.getText();
        String ape = jTextField3.getText();
        String sex = jTextField4.getText();
        String est = jTextField5.getText();
        Date fech_naci = jDateChooser1.getDate();
        int tip = jComboBox1.getSelectedIndex();
        double salar = Double.parseDouble(jTextField6.getText());
        double anti = Double.parseDouble(jTextField7.getText());


        if (tip == 1) {
            Nombramiento_T1H2 nom1 = new Nombramiento_T1H2(salar, anti, anti, tip, idcod, nom, ape,
                    sex, fech_naci, est);
            nom1.validar_anticipo();
            nom1.imprimir();
        }

        if (tip == 2) {
            Comisionados_T1H2 com1 = new Comisionados_T1H2(salar, tip, idcod, nom, ape, sex, fech_naci, est);
            com1.imprimir();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

