    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        String path=Global.getPath();
        //Icono de la aplicación
        ImageIcon icon = new ImageIcon(path+ "\\Recursos\\icono app.png");
        setIconImage(icon.getImage());  
        setExtendedState(this.getExtendedState() | frmPrincipal.MAXIMIZED_BOTH); //maximizar
        //agregar todos los paneles creados
        panelCentral.add(new panelEstudiante(),"Estudiante");
        panelCentral.add(new panelCurso(),"Curso");
    }//GEN-LAST:event_formWindowActivated

